package Grafos.Parcial_Grafos.ParcialesRepetidos.p1;

public class RecintoTemaiken {

    private String nombre;
    private int tiempo;

    public RecintoTemaiken(String nombre, int tiempo) {
        this.nombre = nombre;
        this.tiempo = tiempo;
    }

    public String getNombre() {
        return nombre;
    }

    public int getTiempo() {
        return tiempo;
    }

}
